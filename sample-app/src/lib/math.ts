/**
 * Adds two numbers together
 *
 * @param a - The first number
 * @param b - The second number
 * @returns The sum of a and b
 *
 * @example
 * add(2, 3) // returns 5
 * add(-5, 10) // returns 5
 * add(0.1, 0.2) // returns 0.3
 */
export function add(a: number, b: number): number {
  return a + b;
}
